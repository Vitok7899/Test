 

public class TransmissionModel {
    public int idTransmission;
    public String gearbox;
    public String numberGears;
    public String typeDrive;

    public int getIdTransmission() {
        return idTransmission;
    }

    public void setIdTransmission(int idTransmission) {
        this.idTransmission = idTransmission;
    }

    public String getGearbox() {
        return gearbox;
    }

    public void setGearbox(String gearbox) {
        this.gearbox = gearbox;
    }

    public String getNumberGears() {
        return numberGears;
    }

    public void setNumberGears(String numberGears) {
        this.numberGears = numberGears;
    }

    public String getTypeDrive() {
        return typeDrive;
    }

    public void setTypeDrive(String typeDrive) {
        this.typeDrive = typeDrive;
    }
}
