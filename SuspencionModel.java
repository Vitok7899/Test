 

public class SuspencionModel {
    public int idSuspencion;
    public String frontSuspension;
    public String rearSuspension;
    public String frontBreak;
    public String rearBreak;

    public int getIdSuspencion() {
        return idSuspencion;
    }

    public void setIdSuspencion(int idSuspencion) {
        this.idSuspencion = idSuspencion;
    }

    public String getFrontSuspension() {
        return frontSuspension;
    }

    public void setFrontSuspension(String frontSuspension) {
        this.frontSuspension = frontSuspension;
    }

    public String getRearSuspension() {
        return rearSuspension;
    }

    public void setRearSuspension(String rearSuspension) {
        this.rearSuspension = rearSuspension;
    }

    public String getFrontBreak() {
        return frontBreak;
    }

    public void setFrontBreak(String frontBreak) {
        this.frontBreak = frontBreak;
    }

    public String getRearBreak() {
        return rearBreak;
    }

    public void setRearBreak(String rearBreak) {
        this.rearBreak = rearBreak;
    }
}
