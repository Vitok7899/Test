 

public class PerformanceIndicatorModel {
    public int idPerformanceIndicator;
    public int maxSpeed;
    public String fuelConsumption;

    public int getIdPerformanceIndicator() {
        return idPerformanceIndicator;
    }

    public void setIdPerformanceIndicator(int idPerformanceIndicator) {
        this.idPerformanceIndicator = idPerformanceIndicator;
    }

    public int getMaxSpeed() {
        return maxSpeed;
    }

    public void setMaxSpeed(int maxSpeed) {
        this.maxSpeed = maxSpeed;
    }

    public String getFuelConsumption() {
        return fuelConsumption;
    }

    public void setFuelConsumption(String fuelConsumption) {
        this.fuelConsumption = fuelConsumption;
    }
}
