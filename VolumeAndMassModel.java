 

public class VolumeAndMassModel {
    public int idVolumeAndMass;
    public int trunkVolume;
    public int fuelTankVolume;
    public int curbWeight;
    public int fullMass;

    public int getIdVolumeAndMass() {
        return idVolumeAndMass;
    }

    public void setIdVolumeAndMass(int idVolumeAndMass) {
        this.idVolumeAndMass = idVolumeAndMass;
    }

    public int getTrunkVolume() {
        return trunkVolume;
    }

    public void setTrunkVolume(int trunkVolume) {
        this.trunkVolume = trunkVolume;
    }

    public int getFuelTankVolume() {
        return fuelTankVolume;
    }

    public void setFuelTankVolume(int fuelTankVolume) {
        this.fuelTankVolume = fuelTankVolume;
    }

    public int getCurbWeight() {
        return curbWeight;
    }

    public void setCurbWeight(int curbWeight) {
        this.curbWeight = curbWeight;
    }

    public int getFullMass() {
        return fullMass;
    }

    public void setFullMass(int fullMass) {
        this.fullMass = fullMass;
    }
}
